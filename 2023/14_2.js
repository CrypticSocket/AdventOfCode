let cache = {}

var TiltInDirection = function(data, northSouth, eastWest)
{
    let movement = 0
    // console.log(data.join('\n'))
    do{
        movement = 0
        for(let i = 0; i < data.length; i++)
        {
            if((northSouth == 1 && i == 0) || (northSouth == -1 && i == data.length - 1)) continue
            for(let j = 0; j < data[i].length; j++)
            {
                if((eastWest == 1 && j == 0) || (eastWest == -1 && j == data[i].length - 1)) continue
                if(data[i][j] == 'O' && data[i-northSouth][j-eastWest] == '.')
                {
                    movement++
                    data[i][j] = '.'
                    data[i-northSouth][j-eastWest] = 'O'
                }
            }
        }

    } while(movement > 0)
}

var CalculateLoad = function(data)
{
    let load = 0
    for (let i = 0; i < data.length; i++) {
        for (let j = 0; j < data[i].length; j++) {
            
            if(data[i][j] == 'O')
            {
                load += data.length - i
            }
            
        }        
    }
    return load
}

var PerformCycles = function(data, cycles)
{
    let northsouth = [1, 0 , -1, 0]
    let eastwest = [0, 1, 0, -1]
    let iterator = 0
    while(cycles > 0)
    {
        let key = data.map(a => a.join('')).join('')
        if(cache[key] !=  undefined)
        {
            [data, lastCycle] = cache[key]
            cycles = cycles % (lastCycle - cycles)
        }
        while(iterator != 4)
        {
            TiltInDirection(data, northsouth[iterator], eastwest[iterator])
            iterator++
        }
        if(cache[key] ==  undefined)
        {
            cache[key] = [data, cycles]
        }
        iterator = 0
        cycles--
    }
}

var formatInput = function(input)
{
    let data = input.split('\n').map(i => i.split(''))
    PerformCycles(data, 1000000000)
    return CalculateLoad(data)
}

